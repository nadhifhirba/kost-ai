export const buildGeneratedDesign = (input: {
  roomType: string;
  dimensions: string;
  style: string;
  color: string;
}) => {
  const id = `${formatId(input.roomType)}-${formatId(input.dimensions)}-${formatId(input.style)}-${Date.now().toString(36)}`;
  const title = `${input.roomType} ${input.style}`;

  const paletteMap: Record<string, [string, string, string]> = {
    Minimalis: ['#10121B', '#8B5CF6', '#D8B4FE'],
    Japandi: ['#18161D', '#A78BFA', '#C6F6D5'],
    Industrial: ['#151515', '#F59E0B', '#E5E7EB'],
    Skandinavia: ['#0F172A', '#60A5FA', '#DBEAFE'],
    Bohemian: ['#25153A', '#EC4899', '#FDE68A'],
    Modern: ['#121826', '#8B5CF6', '#22D3EE'],
  };

  const colors = paletteMap[input.style] ?? paletteMap.Minimalis;
  return {
    id,
    roomType: input.roomType,
    style: input.style,
    dimensions: input.dimensions,
    prompt: `${input.roomType} ${input.style} untuk ruang ${input.dimensions} dengan preferensi warna ${input.color}. Fokus pada solusi hemat tempat, lighting lembut, dan produk yang mudah dibeli di marketplace Indonesia.`,
    imageUrl: roomSvg({
      title,
      colors,
      label: `${input.color} • compact layout • AI draft`,
    }),
    products: [
      {
        name: `${input.style} sofa / bed compact`,
        price: 1890000,
        affiliateUrl: tokopediaUrl(`${input.style.toLowerCase()} compact furniture`),
        shop: 'Tokopedia',
      },
      {
        name: 'Rak multifungsi hemat tempat',
        price: 430000,
        affiliateUrl: shopeeUrl('rak multifungsi hemat tempat'),
        shop: 'Shopee',
      },
      {
        name: 'Lampu ambient hangat',
        price: 275000,
        affiliateUrl: tokopediaUrl('lampu ambient hangat minimalis'),
        shop: 'Tokopedia',
      },
    ] as Product[],
    createdAt: new Date().toISOString(),
  } satisfies Design;
};

export const useDesignStore = create<DesignState>((set) => ({
  designs: seedDesigns,
  addDesign: (design) => set((state) => ({ designs: [design, ...state.designs] })),
  removeDesign: (id) => set((state) => ({ designs: state.designs.filter((design) => design.id !== id) })),
  clearDesigns: () => set({ designs: seedDesigns }),
}));

export const getDesignById = (id: string) => seedDesigns.find((design) => design.id === id);

export const marketplaceSearch = (shop: 'Tokopedia' | 'Shopee', query: string) =>
  shop === 'Tokopedia' ? tokopediaUrl(query) : shopeeUrl(query);
